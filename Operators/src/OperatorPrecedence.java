public class OperatorPrecedence {
    public static void main(String[] args) {
        // Operator Precedence in Java:
        // 1. Parentheses and Brackets
        // 2. Unary Operators -, ++, --
        // 3. Arithmetic Operators *, /, and %
        // 4. Arithmetic Operators + and -
        // 5. Relational Operators <, <=, >, >=
        // 6. Equality == and !=
        // 7. Logical && and ||
        // 8. Assignment =, +=, -=, *=, etc.

        // Expression
        // Step 1. Division 12 / 3 = 4
        // Step 2. Multiplication 2 * 3 = 6
        // Step 3. Addition 4 + 6 = 10
        // Step 4. Subtraction 10 - 1 = 9
        var a = 12 / 3 + 2 * 3 - 1;
        System.out.println("a = " + a); // => 9


    }
}
