public class ArithmeticOperators {
    public static void main(String[] args) {
        System.out.println("*** Arithmetic Operators ***");

        // compound variable declaration
        int a = 5, b = 3, result = 0;
        // double a = 5, b = 3, result = 0;

        // Addition +
        result = a + b;
        System.out.println("result Addition = " + result);

        // Subtraction -
        result = a - b;
        System.out.println("result Subtraction = " + result);

        // Multiplication *
        result = a * b;
        System.out.println("result Multiplication = " + result);


        // Division /
        result = a / b;
        System.out.println("result Division = " + result);

        // Modulo (division's remainder) %
        result = a % b;
        System.out.println("result Modulo = " + result);

    }
}
